#Faça um programa que peça as 4 notas bimestrais de um aluno e mostre a média aritmética delas.


n1 = float(input('Digite o primeira nota:'))
n2 = float(input('Digite o segunda nota:'))
n3 = float(input('Digite o segunda nota:'))
n4 = float(input('Digite o segunda nota:'))

media = (n1+n2+n3+n4)/4

print('A media aritmetica das notas é:', media)